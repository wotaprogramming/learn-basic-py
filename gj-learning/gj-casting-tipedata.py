# Casting tipe data yaitu merubah tipe data ke tipe yang lain

data_int = 12
print(data_int, " bertipe : ", type(data_int))

# merubah tipe data int diatas
data_float = float(data_int)
data_str = str(data_int)
data_bool = bool(data_int)

print(data_float, " bertipe : ", type(data_float))
print(data_str, " bertipe : ", type(data_str))
print(data_bool, " bertipe : ", type(data_bool))

print("Hal ini berlaku untuk sebaliknya")